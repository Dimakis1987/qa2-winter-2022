package hw1;

public class MyFishingRod {
    private String typeOfRod;
    private String specialFor;
    private int partNumber;
    private String action;
    private int maxGrammsLoading;
    //-----------------------------------------Getters and Setters---------------


    public String getTypeOfRod() {
        return typeOfRod;
    }

    public void setTypeOfRod(String typeOfRod) {
        this.typeOfRod = typeOfRod;
    }

    public String getSpecialFor() {
        return specialFor;
    }

    public void setSpecialFor(String specialFor) {
        this.specialFor = specialFor;
    }

    public int getPartNumber() {
        return partNumber;
    }

    public void setPartNumber(int partNumber) {
        this.partNumber = partNumber;
    }

    public String getAction() {
        return action;
    }

    public void setAction(String action) {
        this.action = action;
    }

    public int getMaxGrammsLoading() {
        return maxGrammsLoading;
    }

    public void setMaxGrammsLoading(int maxGrammsLoading) {
        this.maxGrammsLoading = maxGrammsLoading;
    }
}
