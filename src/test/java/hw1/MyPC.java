package hw1;

public class MyPC {
    private String modelOfPc;
    private String colourOfPc;
    private String operationalSystem ;
    private float ram;
    private float screenResolution;
    //--------------------Getters and Setters------------------------------------

    public String getModelOfPc() {
        return modelOfPc;
    }

    public void setModelOfPc(String modelOfPc) {
        this.modelOfPc = modelOfPc;
    }

    public String getColourOfPc() {
        return colourOfPc;
    }

    public void setColourOfPc(String colourOfPc) {
        this.colourOfPc = colourOfPc;
    }

    public String getOperationalSystem() {
        return operationalSystem;
    }

    public void setOperationalSystem(String operationalSystem) {
        this.operationalSystem = operationalSystem;
    }

    public float getRam() {
        return ram;
    }

    public void setRam(float ram) {
        this.ram = ram;
    }

    public float getScreenResolution() {
        return screenResolution;
    }

    public void setScreenResolution(float screenResolution) {
        this.screenResolution = screenResolution;
    }
}
