package hw1;

public class Calculator {
    private int buttonNumber;
    private String colour;
    private String typeOfCalculator;
    private int displayQuantity;
    private String brandName;
    //--------------------------------Getters and Setters----------------------------

    public int getButtonNumber() {
        return buttonNumber;
    }

    public void setButtonNumber(int buttonNumber) {
        this.buttonNumber = buttonNumber;
    }

    public String getColour() {
        return colour;
    }

    public void setColour(String colour) {
        this.colour = colour;
    }

    public String getTypeOfCalculator() {
        return typeOfCalculator;
    }

    public void setTypeOfCalculator(String typeOfCalculator) {
        this.typeOfCalculator = typeOfCalculator;
    }

    public int getDisplayQuantity() {
        return displayQuantity;
    }

    public void setDisplayQuantity(int displayQuantity) {
        this.displayQuantity = displayQuantity;
    }

    public String getBrandName() {
        return brandName;
    }

    public void setBrandName(String brandName) {
        this.brandName = brandName;
    }
}
