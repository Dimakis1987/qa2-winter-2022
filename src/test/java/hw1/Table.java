package hw1;

public class Table {
    private String typeOfTable;
    private String colour;
    private float height;
    private float width;
    private float lenghth;
    //----------------------------Getters and Setters---------------------------------------

    public String getTypeOfTable() {
        return typeOfTable;
    }

    public void setTypeOfTable(String typeOfTable) {
        this.typeOfTable = typeOfTable;
    }

    public String getColour() {
        return colour;
    }

    public void setColour(String colour) {
        this.colour = colour;
    }

    public float getHeight() {
        return height;
    }

    public void setHeight(float height) {
        this.height = height;
    }

    public float getWidth() {
        return width;
    }

    public void setWidth(float width) {
        this.width = width;
    }

    public float getLenghth() {
        return lenghth;
    }

    public void setLenghth(float lenghth) {
        this.lenghth = lenghth;
    }
}
